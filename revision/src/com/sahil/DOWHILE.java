package com.sahil;

public class DOWHILE {
    public static void main(String[] args) {
 /*
    syntax do while loop:
     intialisation
     do {
     // body
     increment/decrement
     } while (condition);

     */
        int n = 1;
        do {
            System.out.println(n);
            n ++;
        } while (n <= 5);
    }

    }




